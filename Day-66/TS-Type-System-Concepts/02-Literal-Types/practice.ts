// union types

let union: string | number | boolean;
// any value is allow , from string , number , boolean
union = "apple";
union = 2;
union = true;

// literal types

let literal: "mango" | 456 | true | ["hi", "hello"];
// all value is allowed , which is assigned here
literal = 456; // it is allowed
literal = "mango";
literal = true;
literal = ["hi", "hello"];


